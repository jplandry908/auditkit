package evidence

import (
	"fmt"
	"html/template"
	"os"
)

type ScreenshotGuide struct {
	ControlID  string
	Service    string
	Steps      []Step
	ConsoleURL string
	Highlights []Rectangle
}

type Step struct {
	Number      int
	Instruction string
	Screenshot  string
}

type Rectangle struct {
	X      int
	Y      int
	Width  int
	Height int
}

